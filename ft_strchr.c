#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*p;

	i = 0;
	p = (char *)s;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			return (p + i);
		i++;
	}
	return (NULL);
}
